package org.splevo.tests.statements;

public class IfElseMultipleStatements {

    public int elseIf(int a) {
        if (a == 0) {
            return 0 + 0;

        } else if (a == 1) {
            return 1 + 1;

        } else if (a == 2) {
            return 2 + 2;

        } else if (a == 3) {
            return 3 + 3;
        }
    }
}
